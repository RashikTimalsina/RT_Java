class RightTrianglePattern   
{   

	public static void main(String args[])   
{
   
	int i, j, row=6;   		//i for rows and j for columns  and row denotes the number of rows you want to print  


	//outer loop for rows  
		for(i=0; i<row; i++)   
		{   


	//inner loop for columns  
		for(j=0; j<=i; j++)   
		{   

		System.out.print("* ");   			//prints stars

		}   
		
		System.out.println();   			//throws the cursor in a new line after printing each line  


	}   


	}   


	}  

