
class CeilFunction
{

      public static void main(String[] args)
      {
             double x=3.14;
             double y=10;
 
             double z=Math.ceil(y);  //this will display always rounded value of y
              
             double a=Math.ceil(x);  //this will display always rounded value of x
           
           System.out.println(z);
           System.out.println(a); 

     }


}