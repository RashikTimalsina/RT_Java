
class CharAtFunction
{

   public static void main(String[] args)
      {
           String name="Hello";              //Indexing starts with 0;
            char x=name.charAt(0);         //This will display 'H' as a output as starting from 0 index
            char y=name.charAt(2);         //This will display 'l' as a output as starting from 0 index 
 
            System.out.println(x);
            System.out.println(y);	
     }


}