
class AbsoluteValue
{

      public static void main(String[] args)
      {
             double x=3.14;
             double y=-10;
 
             double z=Math.abs(y);  //this will display absolute value of y
              
             double a=Math.abs(x);  //this will display absolute value of x
           
           System.out.println(z);
           System.out.println(a); 

     }


}