import java.lang.*;
class FloorFunction
{

      public static void main(String[] args)
      {
             double x=3.14;
             double y=10;
 
             double z=Math.floor(y);  //this will display always rounded value of y to down
              
             double a=Math.floor(x);  //this will display always rounded value of x to down
           
           System.out.println(z);
           System.out.println(a); 

     }


}