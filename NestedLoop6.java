import java.lang.*;
class NestedLoop6
{
                 public static void main(String[] args)
                  {
                         int row=1;
                          while(row<=5)
                 { 

                             int col=1;
                              while(col<=5)
                    {
                         System.out.print(row);
                              col++;

                      } 

                          System.out.println();
                           row++;
                     } 
     } 


}                

                   