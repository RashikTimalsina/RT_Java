import java.lang.*;
class RoundFunction
{

      public static void main(String[] args)
      {
             double x=3.14;
             double y=10;
 
             double z=Math.round(y);  //this will display rounded value of y
              
             double a=Math.round(x);  //this will display rounded value of x
           
           System.out.println(z);
           System.out.println(a); 

     }


}