import java.lang.*;
class SquareRoot
{

      public static void main(String[] args)
      {
             double x=3.14;
             double y=10;
 
             double z=Math.sqrt(y);  //this will display square root value of y
              
             double a=Math.sqrt(x);  //this will display  square root value of x
           
           System.out.println(z);
           System.out.println(a); 

     }


}