import java.lang.*;
class StringComparison2
{

	public static void main(String[] args)
	{
	
	//By using equals() method;
	

	String s1="RASHIK";
	String s2="rashik";

	System.out.println(s1.equals(s2));		//false
	
	System.out.println(s1.equalsIgnoreCase(s2));	//true



	}




}