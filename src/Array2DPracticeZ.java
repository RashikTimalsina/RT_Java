import java.lang.*;
class Array2DPracticeZ
{
        public static void main(String[] args)
           {
             
            int[][] a={{0,1,2},{3,4,5},{6,7,8 }};
 

               for (int x=0;x<a.length;x++)
                {
                    System.out.println();

                   for(int y=0;y<a[x].length;y++)
                   {
                      System.out.println(a[x][y]);


                    }
 

                }
          }


}