import java.lang.*;
class TypeCasting				//Only done between Integer value and double value
{

	public static void main(String[] args)
	{
					
	int myInt=13;				//Integer to Double Casting
	double myDouble=myInt;			
	
	System.out.println(myInt);			//13
	System.out.println(myDouble);			//13.0
	
	}


}