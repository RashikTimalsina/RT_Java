import java.lang.*;
class MaxMin
{

      public static void main(String[] args)
      {
             double x=3.14;
             double y=-10;
 
             double z=Math.min(x, y);  //this will display minimum value 
             
             double a=Math.max(x, y);  //this will display maximum value

            System.out.println(z);
            System.out.println(a);

     }


}