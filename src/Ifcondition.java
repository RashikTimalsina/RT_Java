import java.lang.*;
class Ifcondition
{

      //if statement performs a block of code if it's condition evaluates to be true
     // else if is used together when a block of code contains parameter
     //else is used when there is no any parameters given



       public static void main(String[] args)
         {
              int age=30;                //Here we declared an age 
              
              if(age>=35)             	// A condition that if age is greater or equal than 35 then only print "Hello"
              {
                
                  System.out.println("Hello!");
              
 
              }
	else if(age<=20)   	// A condition that if age is greater or equal than 20 then only print "Bye"
                {
  
                   System.out.println("Bye!"); 
                  
           
	}


          }


}

