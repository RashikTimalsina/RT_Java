
class EqualsFunction
{

        public static void main(String[] args)
          {
               String name="Rashik";
	
               boolean x=name.equals("Rashik");               //Equals function is used with boolean value to get true or false 
               boolean y=name.equalsIgnoreCase("rashik");  //EqualsIgnoreCase Function is used to ignore the character value

             
             System.out.println(x);
             System.out.println(y);
           

           }



}