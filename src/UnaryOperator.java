import java.lang.*;
class UnaryOperator
{

	public static void main(String[] args)
	{


	int a=10;
	int b=5;
	
	System.out.println(a++);		//Post-Increment ma value change hunxa tara run garda change dekhaudaina
	System.out.println(++a);		//Pre-Increment ma value change(increase) hunxa by 1

	System.out.println(b--);		//Post-Increment ma value change hunxa tara run garda change dekhaudaina
	System.out.println(--b);		//Pre-Increment ma value change(decrease) hunxa by 1
	
	



	}



}