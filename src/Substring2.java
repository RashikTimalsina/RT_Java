import java.lang.*;
class Substring2
{

	public static void main(String[] args)
	{


	//Index -  0123456789
	
	String s="CristianoRonaldo";
		

	System.out.println("Original String: " +s);
	System.out.println("Substring starting from index 9:" +s.substring(9));
	System.out.println("Substring starting from index 0 to 9:" +s.substring(0,9));	//exclude end index ie disply from 0 to 8


	}



}