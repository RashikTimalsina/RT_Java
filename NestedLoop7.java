import java.lang.*;
class NestedLoop7
{
                 public static void main(String[] args)
                  {
                      int row=1;
                       do
                     { 
                             int col=1;
                          do
                          { 
                               System.out.println(col);
                                 col++;
                           
                           }while(col<=5);

                                 System.out.println();
                                 row++;
                     
                           }while(row<=4);
                       
                  }
        
         } 
 
                           