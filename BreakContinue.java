class BreakContinue
{

                  public static void main(String[] args)
                  {
                          for (int i=1;i<=10;i++)
                           {
                                 if(i==3)
                            {
                                  break;       //it stops looping
                               }
                               System.out.println(i);

                            }
             
 
             }


}